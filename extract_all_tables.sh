#!/bin/bash
# 一键执行所有表格提取步骤

set -e  # 遇到错误立即退出

# 检查参数
if [ $# -eq 0 ]; then
    echo "========================================================================"
    echo "一键提取保险计划书所有表格数据"
    echo "========================================================================"
    echo ""
    echo "用法: ./extract_all_tables.sh <OCR文本文件路径>"
    echo ""
    echo "示例:"
    echo "  ./extract_all_tables.sh '中銀集團人壽 保險有限公司.txt'"
    echo ""
    echo "功能:"
    echo "  ✅ 步骤1: 自动提取表格概要"
    echo "  ✅ 步骤2: 自动验证表格合并"
    echo "  ✅ 步骤3: 自动提取表格完整数据"
    echo ""
    echo "输出:"
    echo "  - *_tablesummary.txt    (表格概要)"
    echo "  - *_table1.txt          (表格1概要)"
    echo "  - *_table1_data.json    (表格1完整数据 JSON)"
    echo "  - *_table1_data.txt     (表格1完整数据 可读)"
    echo "  - *_table2_data.json    (表格2完整数据 JSON)"
    echo "  - ..."
    echo ""
    echo "========================================================================"
    exit 1
fi

OCR_FILE="$1"

# 检查文件是否存在
if [ ! -f "$OCR_FILE" ]; then
    echo "❌ 错误: 文件不存在: $OCR_FILE"
    exit 1
fi

echo "========================================================================"
echo "开始处理: $OCR_FILE"
echo "========================================================================"
echo ""

# 步骤1: 提取表格概要
echo "📋 步骤1/3: 提取表格概要"
echo "------------------------------------------------------------------------"
python3 test_extract_tablesummary.py "$OCR_FILE"
echo ""

# 步骤2: 验证表格合并
echo "🔍 步骤2/3: 验证表格合并"
echo "------------------------------------------------------------------------"
python3 verify_table_merge.py "$OCR_FILE"
echo ""

# 步骤3: 提取表格完整数据
echo "📊 步骤3/3: 提取表格完整数据"
echo "------------------------------------------------------------------------"
python3 extract_table_data.py "$OCR_FILE"
echo ""

echo "========================================================================"
echo "✅ 所有步骤执行完成！"
echo "========================================================================"
echo ""
echo "生成的文件:"
BASE_NAME="${OCR_FILE%.*}"
ls -lh "${BASE_NAME}"* 2>/dev/null | grep -E "(tablesummary|table\d+)" | awk '{print "  -", $9, "(" $5 ")"}'
echo ""
